package java_basics;

public class Variables {

	public static void main(String[] args) {
		
		int x =4;  // declaration and assignment 
		
		System.out.println(x);
		x=23;
		System.out.println(x);
		
		System.out.println(x+4);  // + plus is an operator 
		System.out.println(x/4);  // divide  is an operator
		
		
		
		String words; 
		words="This is a sentencee";
		System.out.println( words + "these are some more words");

	}

}
